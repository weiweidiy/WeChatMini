

using System;

/// <summary>
/// This enum contains the available transition states 
/// </summary>
public enum SMTransitionState {
	/// <summary>
	/// fade into the scene
	/// </summary>
	In,
	/// <summary>
	/// fade out of the scene
	/// </summary>
	Out,
	/// <summary>
	/// between out and in.
	/// </summary>
	Hold
}

